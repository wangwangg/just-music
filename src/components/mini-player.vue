<template>
  <div class="mini-player">
    <!-- 歌曲内容 -->
    <div v-if="currentSong.id" class="song">
      <div class="img-wrap">
        <img :src="currentSong.img" />
      </div>
      <div class="content">
        <div class="top">
          <p class="name">{{ currentSong.name }}</p>
          <p class="split">-</p>
          <p class="artists">{{ currentSong.artistsText }}</p>
        </div>
        <div class="time">
          <span class="played-time">{{ formatTime(currentTime) }}</span>
          <span class="split">/</span>
          <span class="total-time">{{
            formatTime(currentSong.duration / 1000)
          }}</span>
        </div>
      </div>
    </div>
    <!-- 控制台 -->
    <div class="control">
      <div @click="togglePlaying" class="play-icon">
        <Icon :type="playIcon" :size="20" />
      </div>
    </div>

    <div></div>
    <div class="progress-bar-wrap">
      <ProgressBar />
    </div>
    <audio
      ref="audio"
      :src="currentSong.url"
      @play="ready"
      @ended="end"
      @timeupdate="updateTime"
    ></audio>
  </div>
</template>

<script type="text/ecmascript-6">
import { mapState, mapMutations } from 'vuex'
import ProgressBar from '@/base/progress-bar'  //进度条
import { formatTime } from '@/utils/common'
export default {
  created () {
    this.formatTime = formatTime
  },
  data () {
    return {
      songReady: false,
      currentTime: 0,
    }
  },
  components: {
    ProgressBar
  },
  methods: {
    togglePlaying () {
      if (!this.currentSong) {
        return
      }
      this.setPlayingState(!this.playing)
    },
    ready () {
      this.songReady = true
    },
    play () {
      this.audio.play()
    },
    pause () {
      this.audio.pause()
    },
    updateTime (e) {
      const time = e.target.currentTime
      this.currentTime = time
    },
    end () {
      this.audio.currentTime = 0
    },
    ...mapMutations(['setPlayingState'])
  },
  watch: {
    currentSong (newSong, oldSong) {
      if (!newSong.id) {
        return
      }
      if (oldSong) {
        if (newSong.id === oldSong.id) {
          return
        }
      }
      if (this.timer) {
        clearTimeout(this.timer)
      }
      this.timer = setTimeout(() => {
        this.play()
      }, 1000);
    },
    playing (newPlaying) {
      this.$nextTick(() => {
        newPlaying ? this.audio.play() : this.audio.pause()
      })
    }
  },
  computed: {
    playIcon () {
      return this.playing ? 'pause' : 'play'
    },
    audio () {
      return this.$refs.audio
    },
    ...mapState(['currentSong', 'playing'])
  },

}
</script>

